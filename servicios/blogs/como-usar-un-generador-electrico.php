                    <p class="mt-5">Hay una serie de aspectos que debemos tener en cuenta al utilizar un generador eléctrico:</p>
                    <ul>
                        <li class="mb-3"><b>Cantidad de combustible almacenado</b> – Esta cantidad depende de las normas locales o de cada país, por lo que debemos consultar las regulaciones de almacenamiento.</li>
                        <li class="mb-3"><b>Aléjate del generador si te mareas</b> – El monóxido de carbono es un gas peligroso, de forma que si te sientes mareado o débil al estar cerca del generador eléctrico, aléjate de él.</li>
                        <li class="mb-3"><b>No toques el generador con las manos mojadas</b> – Al igual que con otros dispositivos eléctricos, esta conducta puede electrocutarte.</li>
                        <li class="mb-3">Antes de completar combustible al generador, es recomendable <b>esperar 15 minutos para que se enfríe</b>. Si no, puedes causar un incendio innecesario.</li>
                        <li class="mb-3"><b>No almacenes</b> el generador por tiempos prolongados y con líquidos inflamables en él (combustible/lubricante).</li>
                        <li class="mb-3"><b>No dejes el combustible de recarga cerca del generador.</b></li>
                        <li class="mb-3"><b>Por tu seguridad, ten a la mano un extintor de incendios.</b></li>
