                    <p>Los generadores eléctricos son dispositivos muy útiles ya que siempre nos sacan de apuros como por ejemplo, cuando se produce un corte de electricidad porque la red eléctrica local ha fallado, o simplemente cuando lo necesitamos debido a que donde estamos no hay electricidad, dígase en el bosque, en una casa campestre, o hasta en nuestra caravana.</p>
                    <p>A modo general, las recomendaciones para un generador eléctrico de cualquier tipo son las mismas. Es importante seguirlas y recordarlas, dado que es una máquina que contiene un motor y está energizada con combustible. En este caso hablaremos de los combustibles fósiles (gasolina o diésel).</p>
